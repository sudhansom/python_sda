# print from 1 to 10:
for i in range(1, 11):
    print(f"{i} ", end='')
print("")

# print all the even numbers upto 11:
for i in range(1, 12, 2):
    print(f"{i} ", end='')
print("")

# print all the even numbers upto 100
for i in range(0, 101, 2):
    print(f"{i} ", end='')
print("")

# print all the negative odd numbers -100 to 0
for i in range(-99, 3, 2):
    print(f"{i} ", end='')
print("")

# print like : 10, 15, 20 .... upto 100
for i in range(10, 101, 5):
    print(f"{i} ", end='')
print("")
